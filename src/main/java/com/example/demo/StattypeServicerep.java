    @FXML
    void backstat(ActionEvent event) throws  Exception {
        try {
            Parent adminRoot = FXMLLoader.load(getClass().getResource("/Gestionadminservicerep.fxml"));
            Scene adminScene = new Scene(adminRoot);
            Stage window = (Stage) ((Node) event.getSource()).getScene().getWindow();
            window.setScene(adminScene);
            window.show();
        } catch (IOException e) {
            e.printStackTrace();
            showAlert("Erreur de chargement", "Une erreur s'est produite lors du chargement de la page.", Alert.AlertType.ERROR);
        }
    }
    private void showAlert(String title, String content, Alert.AlertType alertType) {
        Alert alert = new Alert(alertType);
        alert.setTitle(title);
        alert.setHeaderText(null);
        alert.setContentText(content);
        alert.showAndWait();
    }
    @FXML
    void quitstat(ActionEvent event) {
        // Fermer la fenêtre principale de l'application
        Stage stage = (Stage) ((Node) event.getSource()).getScene().getWindow();
        stage.close();
        // Arrêter complètement l'application JavaFX
        Platform.exit();
        // Terminer le processus Java
        System.exit(0);
    }


